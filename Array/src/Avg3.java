
public class Avg3 {

	public static void main(String[] args) {
		int ar[]={25,47,58,69,52};
		System.out.println("num  length"+ar.length);
		for(int i=0;i<ar.length;i++){
			System.out.println(i+"----->"+ar[i]);
		}
		for(int i=ar.length-1;i>=0;i--){
			System.out.println(i+"--->"+ar[i]);
		}
	}

}
